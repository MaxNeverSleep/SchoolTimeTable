package com.android.mjutimetable.Bean;

public class ResultBean {

    String classYear;
    String classTerm;

    String className;
    String classType;
    String classCredit;
    String classResult;
    String classMakeUpResult;
    String classRetakeResult;

    public String getClassYear() {
        return classYear;
    }

    public void setClassYear(String classYear) {
        this.classYear = classYear;
    }

    public String getClassTerm() {
        return classTerm;
    }

    public void setClassTerm(String classTerm) {
        this.classTerm = classTerm;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getClassType() {
        return classType;
    }

    public void setClassType(String classType) {
        this.classType = classType;
    }

    public String getClassCredit() {
        return classCredit;
    }

    public void setClassCredit(String classCredit) {
        this.classCredit = classCredit;
    }

    public String getClassResult() {
        return classResult;
    }

    public void setClassResult(String classResult) {
        this.classResult = classResult;
    }

    public String getClassMakeUpResult() {
        return classMakeUpResult;
    }

    public void setClassMakeUpResult(String classMakeUpResult) {
        this.classMakeUpResult = classMakeUpResult;
    }

    public String getClassRetakeResult() {
        return classRetakeResult;
    }

    public void setClassRetakeResult(String classRetakeResult) {
        this.classRetakeResult = classRetakeResult;
    }
}
