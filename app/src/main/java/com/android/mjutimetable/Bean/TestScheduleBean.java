package com.android.mjutimetable.Bean;

public class TestScheduleBean {

    String testClassName;
    String testClassRoom;
    String testTime;
    String testSitNum;
    String StuName;

    public String getTestClassName() {
        return testClassName;
    }

    public void setTestClassName(String testClassName) {
        this.testClassName = testClassName;
    }

    public String getTestClassRoom() {
        return testClassRoom;
    }

    public void setTestClassRoom(String testClassRoom) {
        this.testClassRoom = testClassRoom;
    }

    public String getTestTime() {
        return testTime;
    }

    public void setTestTime(String testTime) {
        this.testTime = testTime;
    }

    public String getTestSitNum() {
        return testSitNum;
    }

    public void setTestSitNum(String testSitNum) {
        this.testSitNum = testSitNum;
    }

    public String getStuName() {
        return StuName;
    }

    public void setStuName(String stuName) {
        StuName = stuName;
    }
}

